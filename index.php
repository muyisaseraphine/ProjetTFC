                a initié la création d’une mutuelle de santé dans son Diocèse en vue de répondre tant soit peu à
                ce problème très crucial. Il répondait aussi à un des objectifs principaux que s’est fixé la
                Conférence Episcopale Nationale du Congo, CENCO en sigle , en matière de santé en 2008, à
                savoir créer des Mutuelles de santé dans les 47 Diocèses du Congo . Pour la mise en œuvre de
                ce projet, SE Mgr SIKULI Melchisédech l’a confié au Bureau Diocésain des Œuvres Médicales,
